day_no = int(input("Enter day no.: "))

if day_no == 1:
    print("Sunday")
elif day_no == 2:
    print("Monday")
elif day_no == 3:
    print("Tuesday")
elif day_no == 4:
    print("Wednesday")
elif day_no == 5:
    print("Thursday")
elif day_no == 6:
    print("Friday")
elif day_no == 7:
    print("Saturday")
else:
    print("Invalid Input")